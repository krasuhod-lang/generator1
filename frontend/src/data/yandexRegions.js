/**
 * Поиск регионов по подстроке (русск.) или по коду.
 * Возвращает не более `limit` элементов в исходном порядке справочника
 * (то есть сначала округа/области, потом города), что облегчает выбор
 * родительского региона.
 */
export function searchRegions(query, { limit = 50 } = {}) {
  const q = String(query || '').trim().toLowerCase();
  if (!q) return YANDEX_REGIONS.slice(0, limit);
  const out = [];
  for (const r of YANDEX_REGIONS) {
    if (r.name.toLowerCase().includes(q) || String(r.code).includes(q)) {
      out.push(r);
      if (out.length >= limit) break;
    }
  }
  return out;
}

/** Поиск региона по коду lr — для отображения «текущего» названия. */
export function findRegionByCode(code) {
  const c = String(code || '').trim();
  if (!c) return null;
  return YANDEX_REGIONS.find((r) => String(r.code) === c) || null;
}

export default YANDEX_REGIONS;
