'use strict';

/**
 * russianStem — упрощённый стеммер для русского языка.
 * Перенесено из index.html без изменений.
 *
 * Отсекает распространённые словоизменительные окончания,
 * возвращает корневую форму слова (не менее 4 символов).
 *
 * @param {string} word
 * @returns {string} — корень слова в нижнем регистре
 */
function russianStem(word) {
  const w = word.toLowerCase().trim();

  const endings = [
    'ающего','ающему','ающими','ающих','ающий','ающим','ающие',
    'ующего','ующему','ующими','ующих','ующий','ующим','ующие',
    'ившего','ившему','ившими','ившего','ившее','ившая','ившие',
    'ованных','ованному','ованным','ованного','ованное','ованной','ованная','ованные','ованный',
    'ующим','ующей','ующих','ующие','ующий','ующего',
    'ениями','ениях','ению','ений','ения','ением','ение',
    'остями','остях','ости','остью','остей',
    'ающий','ающая','ающее','ающие',
    'ьного','ьному','ьными','ьного','ьном','ьная','ьные','ьный','ьным','ьной',
    'нного','нному','нными','нного','нном','нная','нные','нный','нным','нной',
    'ельно','ельный','ельная','ельное','ельных','ельными','ельным',
    'ально','альный','альная','альное','альных',
    'ивать','овать','евать','ывать',
    'ивает','овает','евает','ывает',
    'ают','яют','уют','ают','ет','ят','ит',
    'ами','ями','ых','их','ой','ей','ому','ему',
    'ых','их','ого','его','ому','ему',
    'ами','ями','ах','ях',
    'ую','юю','ие','ые','ий','ый',
    'ая','яя','ое','ее',
    'ова','ева','ива','ыва',
    'ью','ей','ой',
    'ем','ом','ам','ям',
    'ей','ий','ый',
    'их','ых',
    'ть','ти','ча',
    'ые','ие','ой','ей',
    'ого','его',
    'ам','ям',
    'ом','ем',
    'ах','ях',
    'ал','ял','ил','ул',
    'ая','яя',
    'ны','на','но',
    'ов','ев',
    'ей','ой',
    'ую','юю',
    'я','а','о','и','е','у','ю','ь',
  ];

  for (const end of endings) {
    if (w.length > end.length + 3 && w.endsWith(end)) {
      return w.slice(0, w.length - end.length);
    }
  }

  return w;
}

module.exports = { russianStem };
