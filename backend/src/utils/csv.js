'use strict';

/**
 * Утилиты для генерации CSV (мета-теги, relevance, category-lead, ...).
 *
 * Контекст бага «косяк парсинга csv у мета-тегов»: экспорты использовали
 * `;` как разделитель (Excel RU-локали ожидает `;`), но Excel EN, Google
 * Sheets и LibreOffice по умолчанию берут `,`. Без подсказки они кладут
 * всю строку в одну колонку — пользователь жалуется на «нечитаемый csv».
 *
 * Решение: первой строкой пишем `sep=;` — это Excel-/Sheets-специфичная
 * директива, которую все три приложения распознают и принудительно
 * переключаются на указанный разделитель. BOM сохраняем перед директивой —
 * UTF-8 определяется корректно.
 *
 * Дополнительно:
 *   • `csvCell` экранирует кавычки, режет CR/LF и навешивает антифрод-
 *     префикс на формулы (CSV-injection guard).
 *   • Все ячейки оборачиваются в `"…"` — это безопасно даже когда внутри
 *     встречается разделитель (`;`/`,`).
 */

const DEFAULT_SEP = ';';
const BOM = '\uFEFF';

function csvCell(val) {
  let s = val == null ? '' : String(val);
  // CRLF внутри значений ломают строки CSV — заменяем на пробел.
  s = s.replace(/[\r\n]+/g, ' ');
  // CSV-injection guard: =/+/-/@ в начале — префиксуем апострофом.
  if (/^[=+\-@]/.test(s)) s = `'${s}`;
  return `"${s.replace(/"/g, '""')}"`;
}

/**
 * Возвращает «шапку» CSV-файла: BOM + `sep=...` директива. После этой
 * строки уже идут реальные заголовки колонок. Использовать так:
 *
 *   const sep = ';';
 *   let csv = csvHeader(sep);
 *   csv += headers.map(csvCell).join(sep) + '\r\n';
 *   …
 */
function csvHeader(sep = DEFAULT_SEP) {
  // Директива `sep=` должна быть в первой строке файла; согласно
  // документации Excel, BOM перед ней допустим (и нужен — иначе кириллица
  // ломается на Windows). Сам разделитель в директиве НЕ экранируется.
  return `${BOM}sep=${sep}\r\n`;
}

module.exports = { csvCell, csvHeader, DEFAULT_SEP };
