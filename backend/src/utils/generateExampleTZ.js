// ── Вспомогательные функции для формирования документа ──────────────

function sectionHeading(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 400, after: 200 },
    border: {
      bottom: { style: BorderStyle.SINGLE, size: 1, color: 'CCCCCC' },
    },
    children: [
      new TextRun({
        text,
        bold: true,
        size: 26,
        font: 'Arial',
        color: '333333',
      }),
    ],
  });
}

function fieldBlock(label, value) {
  const children = [
    new Paragraph({
      spacing: { before: 100, after: 50 },
      children: [
        new TextRun({
          text: label,
          bold: true,
          size: 22,
          font: 'Arial',
          color: '444444',
        }),
      ],
    }),
  ];

  // Split value by newlines to create separate paragraphs
  const lines = value.split('\n');
  for (const line of lines) {
    children.push(
      new Paragraph({
        spacing: { after: 40 },
        indent: { left: 300 },
        children: [
          new TextRun({
            text: line,
            size: 22,
            font: 'Arial',
          }),
        ],
      })
    );
  }

  // Add spacing after the block
  children.push(new Paragraph({ spacing: { after: 100 } }));

  return children;
}

