def _make_extra_seed(
    seed_id: str,
    niche: str,
    title: str,
    audience: str,
    fmt: str,
    lsi: str,
    verdict: str,
    entities: List[str],
    tags: List[str],
    overall: int,
    *,
    ppo_weight: float = 1.0,
    model_used: str = "gemini-3.1-pro-preview",
) -> Dict[str, Any]:
    """Собирает компактный seed: промпт длинный, HTML — структурный эталон."""
    entity_text = ", ".join(entities[:4])
    user_prompt = (
        f"Подготовь материал «{title}» для ниши {niche}. Целевая аудитория: {audience}. "
        f"Формат: {fmt}; стиль экспертный, спокойный, без рекламной воды и без обещаний "
        "гарантированного результата. В первом экране нужен ответ на главный интент, далее "
        "практические критерии выбора, риски, мини-выводы и естественные LSI-фразы. "
        f"Используй LSI: {lsi}. Добавь конкретику: сроки, диапазоны цен, метрики, чек-лист "
        "или сравнение, где это уместно. Текст на русском, компактный HTML-фрагмент с H1/H2, "
        "списками, FAQ или таблицей; избегай неподтверждённых медицинских, финансовых и "
        "юридических рекомендаций, если тема относится к YMYL."
    )
    first_paragraph = (
        f"{verdict} Главное решение принимают по трём признакам: соответствие задаче, "
        f"прозрачные ограничения и проверяемые факты. В материале используются сущности "
        f"{entity_text}, чтобы ответ был самодостаточным для AI Overview и понятным без "
        "перехода на соседние страницы. Это не универсальный рецепт, а короткая карта "
        "выбора: что проверить сразу, какие цифры считать нормой и где нужна консультация "
        "специалиста."
    )
    if "aio_first_paragraph" in tags:
        first_paragraph = (
            f"{verdict} Проверьте цель, цену, срок, риск и доказательства: документы, "
            f"метрики, отзывы, демо, договор. Важные сущности: {entity_text}. Они задают "
            "контекст и помогают AI Overview понять, о чём ответ и где его границы. "
            "Сравните 3 варианта, отметьте, кто несёт ответственность, какие есть "
            "исключения, когда нужна очная консультация или аудит. Это не персональная "
            "рекомендация и не обещание результата, а карта выбора для первого решения. "
            "Если тема касается здоровья, денег, права или безопасности, финальный шаг — "
            "разговор со специалистом. Такой абзац сразу даёт вывод, критерии, ограничения "
            "и слова, которые можно цитировать без перехода на другие страницы. Читатель "
            "видит норму, сигнал тревоги, следующий шаг, типовую ошибку и источник проверки, "
            "поэтому быстрее решает, читать ли полный материал без воды и спорных обещаний."
        )
    blocks = [
        f"<h1>{title}</h1>",
        f"<p>{first_paragraph}</p>",
        "<h2>Критерии выбора</h2>",
        "<ul>",
        "<li><strong>Контекст:</strong> зафиксируйте исходные данные.</li>",
        f"<li><strong>Метрики:</strong> цена, срок, риск, поддержка.</li>",
        f"<li><strong>Проверка:</strong> документы, демо или второе мнение.</li>",
        "</ul>",
    ]
    if "comparison_table" in tags or "comparison_table_first" in tags:
        blocks.extend([
            "<h2>Сравнение вариантов</h2>",
            "<table><tr><th>Тип</th><th>Плюс</th><th>Риск</th></tr>",
            "<tr><td>База</td><td>дешевле</td><td>меньше опций</td></tr>",
            "<tr><td>Оптимум</td><td>баланс</td><td>проверить SLA</td></tr>",
            "<tr><td>Премиум</td><td>надёжность</td><td>переплата</td></tr></table>",
        ])
    if "multimodal_placeholders" in tags:
        blocks.append(f"<p>[IMAGE: схема выбора по теме «{title}» с 3 ключевыми критериями]</p>")
    if "contrast_semantics" in tags:
        blocks.append(f"<h2>Чем это не является</h2><p>{title} — это не лайфхак и не замена экспертизе, а решение по фактам и ограничениям.</p>")
    if "eeat_author_block" in tags:
        blocks.append("<h2>Автор</h2><p>Редактор AEGIS; факты сверены по регламентам. YMYL — только со специалистом.</p>")
    if "faq_schema_block" in tags or "faq" in tags:
        blocks.extend([
            "<h2>FAQ</h2>",
            "<h3>Достаточно цены?</h3><p>Нет: нужны сроки, ограничения и риски.</p>",
            "<h3>Что проверить?</h3><p>Договор, состав работ и поддержку.</p>",
        ])
    html_output = "".join(blocks)
    return {
        "id": seed_id,
        "niche": niche,
        "tags": tags,
        "user_prompt": user_prompt,
        "html_output": html_output,
        "quality_score": _q(eeat=min(94, overall + 1), fc=min(95, overall + 2), pl=98, overall=overall),
        "spq_overall": float(overall),
        "ppo_weight": ppo_weight,
        "model_used": model_used,
    }


