def load_seed_dataset(niche: Optional[str] = None) -> List[Dict[str, Any]]:
    """Возвращает копию seed-датасета, отфильтрованную по нише.

    Args:
        niche: если None — возвращает все seed-строки. Иначе — только те,
               у кого niche совпадает (case-insensitive).

    Returns:
        Список dict'ов. Каждый dict — готовый ряд для INSERT'а в
        aegis_dspy_dataset с is_seed=TRUE.
    """
    if not niche:
        return [dict(r) for r in _SEEDS]
    n = niche.strip().lower()
    return [dict(r) for r in _SEEDS if (r.get("niche") or "").lower() == n]


def count_seeds() -> int:
    return len(_SEEDS)


def seed_niches() -> List[str]:
    """Список уникальных ниш в seed-датасете (для UI/диагностики)."""
    seen: List[str] = []
    for r in _SEEDS:
        n = r.get("niche")
        if n and n not in seen:
            seen.append(n)
    return seen
