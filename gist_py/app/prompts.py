"""Переиспользуемые промпты пайплайна (G0–G3) — дословно из ТЗ.

Каждый промпт — шаблон str.format(); подстановки экранировать не нужно,
render() подставляет только переданные ключи.
"""

from __future__ import annotations

import re


def render(template: str, **kwargs) -> str:
    """Подставить переменные в шаблон промпта.

    Заменяются только простые {имена} из kwargs — фигурные скобки JSON-примеров
    и незаполненные плейсхолдеры остаются как есть.
    """
    def repl(match: "re.Match[str]") -> str:
        key = match.group(1)
        return str(kwargs[key]) if key in kwargs else match.group(0)

    return re.sub(r"\{([a-zA-Z_][a-zA-Z0-9_]*)\}", repl, template)


