-- 091_project_share_positions.sql
-- Часть плана «Съём позиций внутри проекта». Добавляем флаг, который
-- определяет, включать ли в публичный (share-link) ответ проекта секцию
-- по позициям (`positions: { summary, series, tops_distribution, … }`).
--
-- Зачем флаг, а не вечно включено:
--   • владелец проекта может захотеть расшарить дашборд клиенту, но скрыть
--     запросы из съёма (например, NDA, конкурентная разведка),
--   • дефолт — TRUE: для большинства проектов клиент должен видеть динамику
--     по топам, иначе теряется смысл встраивания съёма позиций в проект.
--
-- Старые проекты автоматически получают TRUE (DEFAULT). Если позиций нет —
-- секция в публичном ответе будет просто пустой, без отдельной защиты.

ALTER TABLE projects
  ADD COLUMN IF NOT EXISTS share_includes_positions BOOLEAN NOT NULL DEFAULT TRUE;
