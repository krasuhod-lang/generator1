-- =================================================================
-- Migration 020: Relevance — store processed_documents in Postgres
-- as a fallback for когда Redis-кэш недоступен или истёк.
-- =================================================================
-- Заказчик: «семантические коконы не работают» — корневая причина в том,
-- что коконы могли строиться только пока был жив Redis-кэш по ключу
-- relevance:raw:{report_id}. Если Redis недоступен (например, не поднят
-- в окружении заказчика) или TTL истёк — пересчёт коконов был невозможен.
--
-- Теперь компактный список processed-документов (только леммы — POS-
-- последовательности нам для коконов не нужны) дублируется в Postgres
-- в колонке raw_processed JSONB. Это:
--   * гарантирует, что коконы всегда работают на готовом отчёте;
--   * не сильно раздувает БД (леммы уже отфильтрованы от стоп-слов и
--     пунктуации, типичный размер 50-300 КБ на отчёт);
--   * Redis-кэш при наличии остаётся как «горячий путь» (быстрее, не
--     гоняет JSONB по сети).
-- =================================================================

ALTER TABLE relevance_reports
  ADD COLUMN IF NOT EXISTS raw_processed JSONB;

-- Партичный индекс «у каких отчётов есть DB-fallback» — пригодится для
-- быстрых выборок «сколько отчётов могут пересчитать коконы».
CREATE INDEX IF NOT EXISTS idx_relevance_reports_raw_processed_present
  ON relevance_reports (id)
  WHERE raw_processed IS NOT NULL;
