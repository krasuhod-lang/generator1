-- ─────────────────────────────────────────────────────────────────────────────
-- Миграция 124: прогрев домена Outreach — новый график рассылки.
--
-- Правила (зашитая логика прогрева):
--   Неделя 1: 25 писем/день,  не более 3 писем/час
--   Неделя 2: 50 писем/день,  не более 6 писем/час
--   Неделя 3: 100 писем/день, не более 12 писем/час
--   Неделя 4: 250 писем/день, не более 30 писем/час
--   Неделя 5: 500 писем/день, не более 55 писем/час
-- Отправка равномерно распределяется в окне 07:00–18:00 МСК.
--
-- Файл идемпотентен (безопасно применять повторно).
-- ─────────────────────────────────────────────────────────────────────────────

-- 1. Часовой лимит в справочнике прогрева.
ALTER TABLE outreach_warmup_schedule
  ADD COLUMN IF NOT EXISTS hourly_limit INTEGER NOT NULL DEFAULT 3;

-- 2. Новые лимиты по неделям (перезаписываем существующие строки).
INSERT INTO outreach_warmup_schedule (week_number, daily_limit, hourly_limit, description) VALUES
  (1, 25,  3,  'Прогрев: 1-я неделя — 25 писем/день, до 3 писем/час, окно 07:00–18:00 МСК'),
  (2, 50,  6,  'Прогрев: 2-я неделя — 50 писем/день, до 6 писем/час'),
  (3, 100, 12, 'Прогрев: 3-я неделя — 100 писем/день, до 12 писем/час'),
  (4, 250, 30, 'Прогрев: 4-я неделя — 250 писем/день, до 30 писем/час'),
  (5, 500, 55, 'Прогрев: 5-я неделя — 500 писем/день, до 55 писем/час')
ON CONFLICT (week_number) DO UPDATE
  SET daily_limit  = EXCLUDED.daily_limit,
      hourly_limit = EXCLUDED.hourly_limit,
      description  = EXCLUDED.description;

-- 3. Поднимаем потолок дневного лимита кампаний до 500, чтобы график
--    прогрева был источником истины (планировщик берёт MIN(campaign.daily_limit,
--    warmup.daily_limit)). Существующие кампании с прежним дефолтом 30 не должны
--    ограничивать недели 3–5.
ALTER TABLE outreach_campaigns ALTER COLUMN daily_limit SET DEFAULT 500;
UPDATE outreach_campaigns SET daily_limit = 500 WHERE daily_limit < 500;
